package com.atf.cap.business;

public class SettingInfo {
	private String serverIP = null;
	private String serverPort = null;
	private String paymentServerIP = null;
	private String paymentServerPort= null;
	private String baffleServerIP= null;
	private String baffleServerPort= null;
	public String getServerIP() {
		return serverIP;
	}
	public void setServerIP(String serverIP) {
		this.serverIP = serverIP;
	}
	public String getServerPort() {
		return serverPort;
	}
	public void setServerPort(String serverPort) {
		this.serverPort = serverPort;
	}
	public String getPaymentServerIP() {
		return paymentServerIP;
	}
	public void setPaymentServerIP(String paymentServerIP) {
		this.paymentServerIP = paymentServerIP;
	}
	public String getPaymentServerPort() {
		return paymentServerPort;
	}
	public void setPaymentServerPort(String paymentServerPort) {
		this.paymentServerPort = paymentServerPort;
	}
	public String getBaffleServerIP() {
		return baffleServerIP;
	}
	public void setBaffleServerIP(String baffleServerIP) {
		this.baffleServerIP = baffleServerIP;
	}
	public String getBaffleServerPort() {
		return baffleServerPort;
	}
	public void setBaffleServerPort(String baffleServerPort) {
		this.baffleServerPort = baffleServerPort;
	}
	

}
